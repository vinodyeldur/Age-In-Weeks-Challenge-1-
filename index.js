

function lifeInWeeks(age) {
  var remainingDays = 90 - age;
  var days = remainingDays * 365;
  var weeks = remainingDays * 52;
  var months = remainingDays * 12;

  console.log("You have "+ days + " days, "+ weeks + " weeks and "+ months + " months left ")
    
}
lifeInWeeks(51);
    
    
    
    
    

